package com.mjzk.dome.common;


public class Temporary {

	private String stockId;
	private String mtlNo;
	private String warehouseNo;
	private String shelfNo;
	private String locationNo;
	private String trayNo;
	private Integer qty;
	private String remark;
	private String updateDate;
	private String mtlName;
	private String mtlType;
	
	public Temporary() {
	}
	
	public String getMtlType() {
		return mtlType;
	}

	public void setMtlType(String mtlType) {
		this.mtlType = mtlType;
	}

	public String getStockId() {
		return stockId;
	}
	public void setStockId(String stockId) {
		this.stockId = stockId;
	}
	public String getMtlNo() {
		return mtlNo;
	}
	public void setMtlNo(String mtlNo) {
		this.mtlNo = mtlNo;
	}
	public String getWarehouseNo() {
		return warehouseNo;
	}
	public void setWarehouseNo(String warehouseNo) {
		this.warehouseNo = warehouseNo;
	}
	public String getShelfNo() {
		return shelfNo;
	}
	public void setShelfNo(String shelfNo) {
		this.shelfNo = shelfNo;
	}
	public String getLocationNo() {
		return locationNo;
	}
	public void setLocationNo(String locationNo) {
		this.locationNo = locationNo;
	}
	public String getTrayNo() {
		return trayNo;
	}
	public void setTrayNo(String trayNo) {
		this.trayNo = trayNo;
	}
	public Integer getQty() {
		return qty;
	}
	public void setQty(Integer qty) {
		this.qty = qty;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	public String getMtlName() {
		return mtlName;
	}
	public void setMtlName(String mtlName) {
		this.mtlName = mtlName;
	}
}
